AS
SELECT O.STUDY_CODE 스터디코드, A.APPLY_CODE 참가코드, A.USER_CODE 참여자, C.CANCEL_CODE 취소코드, C.CANCEL_DATE 취소날짜, C.AUTO_CANCEL 자동취소여부, P.POSITION_CODE 직책, O.USER_CODE AS 개설자 
